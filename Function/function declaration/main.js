// function declaration
function bilanganAngka(a, b) {
  var total;
  total = a + b;
  return total;
}
console.log(bilanganAngka(9, 10));

// function declaration menghitung tinggi
function hitung(height, width) {
  return width * height;
}
console.log(hitung(10, 20));

// function declaration pengurangan
function kurang(z, b) {
  return z - b;
}
console.log(kurang(300, 1));

// membuat function sederhana
function membuat(a, b) {
  return a * a * a + b * b * b;
}
alert(membuat(8, 3));
alert(membuat(10, 3));
alert(membuat(90, 3));
